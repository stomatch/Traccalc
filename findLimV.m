function [ index ] = findLimV(lim,s )
%--------------------------------------------------------------------- 
%Функция поиска значения ограничения скорости на участке профиля пути
% Входные параметры:
% lim - матрица ограничений скорости
% s - текущая координата пути
%---------------------------------------------------------------------
j=size(lim);

    for i=1:j(1)
        if (lim(i,1)<=s) && (s<lim(i,2))
            index=lim(i,:);
        end
    end

end

